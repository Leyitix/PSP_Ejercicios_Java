public class ej_001 {
	
//	 Realiza un programa que a partir de dos números (introducidos en el código) muestre su 
//	 suma por pantalla.

	public static void main(String[] args) {

		int num1 = 100;
		int num2 = 23;
		int suma = 0;
		
		suma = num1 + num2;
		
		System.out.println("Suma de " + num1 + " + " + num2 + " es " + suma);
		System.out.println("Suma de " + num1 + " + " + num2 + " es " + (num1 + num2) );

	}

}
